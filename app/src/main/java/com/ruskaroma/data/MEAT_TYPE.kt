package com.ruskaroma.data

/**
 * Enum class representing different types of meat used in dishes.
 * This is used to categorize the kind of meat in a pizza or dish.
 */
enum class MEAT_TYPE {

    /**
     * Represents chicken as the meat type.
     */
    CHICKEN,

    /**
     * Represents beef as the meat type.
     */
    BEEF,

    /**
     * Represents a mixed combination of meats (e.g., chicken and beef).
     */
    MIXED
}
