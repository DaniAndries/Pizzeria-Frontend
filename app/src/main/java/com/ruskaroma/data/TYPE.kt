package com.ruskaroma.data

/**
 * Enum class representing the different types of products in the menu.
 * This enum is used to categorize products such as food and beverages into specific types.
 *
 * @property PIZZA Represents a pizza product.
 * @property DRINK Represents a drink product.
 * @property KEBAB Represents a kebab product.
 * @property PASTA Represents a pasta product.
 */
enum class TYPE {
    PIZZA,
    DRINK,
    KEBAB,
    PASTA
}
