package com.ruskaroma.data.model

/**
 * Enum class representing the possible sizes for products.
 * This enum is used to define the size of a product, such as pizzas or other items,
 * in terms of small, medium, or large.
 *
 * @property SMALL The smallest available size for a product.
 * @property MEDIUM A medium size for a product, typically larger than the small size.
 * @property LARGE The largest available size for a product.
 */
enum class SIZE {
    SMALL,
    MEDIUM,
    LARGE
}
